public class Loops{
  public static void main(String[] args){



    //WHILE LOOP
    System.out.println("While Loop");
    int i = 1;
    while (i <= 10){
      System.out.println(i);
      i++;
    }
    


    //DO WHILE LOOP
    System.out.println("Do While Loop");
    i = 1;
    do {
      System.out.println(i);
      i++;
    } while (i <= 10);



    //FOR LOOP
    System.out.println("For Loop");
    i = 1;
    for ( ; i <= 10 ; i++){
      System.out.println(i);
    }

    
  }
}